package com.risk.result.model;

public class AircraftCheckListDetail {
  double deIce;
  double wetherRadar;
  double stormScope;
  double autoPilot;

  String msgDeIce;
  String msgWetherRadar;
  String msgStormScope;
  String msgAutoPilot;

  public AircraftCheckListDetail() {
    super();
  }

  public double getDeIce() {
    return deIce;
  }

  public void setDeIce(double deIce) {
    this.deIce = deIce;
  }

  public double getWetherRadar() {
    return wetherRadar;
  }

  public void setWetherRadar(double wetherRadar) {
    this.wetherRadar = wetherRadar;
  }

  public double getStormScope() {
    return stormScope;
  }

  public void setStormScope(double stormScope) {
    this.stormScope = stormScope;
  }

  public double getAutoPilot() {
    return autoPilot;
  }

  public void setAutoPilot(double autoPilot) {
    this.autoPilot = autoPilot;
  }

  public String getMsgDeIce() {
    return msgDeIce;
  }

  public void setMsgDeIce(String msgDeIce) {
    this.msgDeIce = msgDeIce;
  }

  public String getMsgWetherRadar() {
    return msgWetherRadar;
  }

  public void setMsgWetherRadar(String msgWetherRadar) {
    this.msgWetherRadar = msgWetherRadar;
  }

  public String getMsgStormScope() {
    return msgStormScope;
  }

  public void setMsgStormScope(String msgStormScope) {
    this.msgStormScope = msgStormScope;
  }

  public String getMsgAutoPilot() {
    return msgAutoPilot;
  }

  public void setMsgAutoPilot(String msgAutoPilot) {
    this.msgAutoPilot = msgAutoPilot;
  }
}
